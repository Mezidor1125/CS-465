// import used to include core angular framework within application (SNHU, 2023, p. 1)
import { Inject, Injectable } from '@angular/core';

// import used to include http angular framework within application (SNHU, 2023, p. 1)
import { Http } from '@angular/http'

// import used to include trip.ts within application (SNHU, 2023, p. 1)
import { Trip } from '../models/trip';

// import used for the user within application (SNHU, 2023, p. 1)
import { User } from '../models/user';

// auth response import within application (SNHU, 2023, p. 1)
import { AuthResponse } from '../models/authresponse';

// browser storage import within application (SNHU, 2023, p. 1)
import { BROWSER_STORAGE } from '../../app_admin/src/app/storage';


@Injectable()

// class used for the trip data service elements within application (SNHU, 2023, p. 1)
export class TripDataService {

  constructor(private http: Http,
    @Inject(BROWSER_STORAGE) private storage: Storage) { }

  private apiBaseUrl = 'http://localhost:3000/api';
  private tripUrl = `${this.apiBaseUrl}/trips/`;

  // public function used to generate trips within application (SNHU, 2023, p. 1)
  public getTrips(): Promise<Trip[]> {
    console.log('Inside TripDataService#getTrips');
    return this.http
      .get(`${this.apiBaseUrl}/trips/`)
      .toPromise()
      .then(response => response.json() as Trip[])
      .catch(this.handleError);
  }

  // public function add trip within application (SNHU, 2023, p. 1)
  public addTrip(formData: Trip): Promise<Trip[]> {
    console.log('Inside TripDataService#addTrip');
    return this.http
      .post(`${this.tripUrl}`, formData)
      .toPromise()
      .then(response => response.json() as Trip[])
      .catch(this.handleError);
  }

  // public function get trip within application (SNHU, 2023, p. 1)
  public getTrip(code: string): Promise<Trip> {
    console.log('Inside TripDataService#getTrip(tripCode)');
    return this.http
      .get(this.tripUrl + code)
      .toPromise()
      .then(response => response.json() as Trip)
      .catch(this.handleError);
  }

  // public function to update trip within application (SNHU, 2023, p. 1)
  public updateTrip(formData: Trip): Promise<Trip> {
    console.log('Inside TripDataService#updateTrip');
    return this.http
      .put(this.tripUrl + formData.code, formData)
      .toPromise()
      .then(response => response.json() as Trip[])
      .catch(this.handleError);
  }

  // public handle error function within application (SNHU, 2023, p. 1)
  private handleError(error: any): Promise<any> {
    console.error('Something has gone wrong', error); // for demo purposes only (SNHU, 2023, p. 1)
    return Promise.reject(error.message || error);
  }

  // public login function within application (SNHU, 2023, p. 1)
  public login(user: User): Promise<AuthResponse> {
    return this.makeAuthApiCall('login', user);
  }

  // public register function within application (SNHU, 2023, p. 1)
  public register(user: User): Promise<AuthResponse> {
    return this.makeAuthApiCall('register', user);
  }

  // public make auth api call function within application (SNHU, 2023, p. 1)
  private makeAuthApiCall(urlPath: string, user: User):
    Promise<AuthResponse> {
    const url: string = `${this.apiBaseUrl}/${urlPath}`;
    return this.http
      .post(url, user)
      .toPromise()
      .then(response => response.json() as AuthResponse)
      .catch(this.handleError);
  }
}
